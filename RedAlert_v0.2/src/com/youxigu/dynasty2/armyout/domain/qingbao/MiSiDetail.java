package com.youxigu.dynasty2.armyout.domain.qingbao;

import java.io.Serializable;

/**
 * 军情详情中一些通用的属性
 * @author a
 *
 */
public abstract class MiSiDetail implements Serializable{
	private static final long serialVersionUID = 5049762063172172448L;
	private String aimIcon; // 目标的资源图片
	private String aimName; // 目标名称
	private int aimPointX; // 目标坐标
	private int aimPointY;
	
	public MiSiDetail(){
		
	}
	public MiSiDetail(String aimIcon,String aimName,int aimPointX,int aimPointY){
		this.aimIcon = aimIcon;
		this.aimName = aimName;
		this.aimPointX = aimPointX;
		this.aimPointY = aimPointY;
	}
	
	public String getAimIcon() {
		return aimIcon;
	}
	public void setAimIcon(String aimIcon) {
		this.aimIcon = aimIcon;
	}
	public String getAimName() {
		return aimName;
	}
	public void setAimName(String aimName) {
		this.aimName = aimName;
	}
	public int getAimPointX() {
		return aimPointX;
	}
	public void setAimPointX(int aimPointX) {
		this.aimPointX = aimPointX;
	}
	public int getAimPointY() {
		return aimPointY;
	}
	public void setAimPointY(int aimPointY) {
		this.aimPointY = aimPointY;
	}
}
