package com.youxigu.dynasty2.hero.proto;

import com.youxigu.dynasty2.chat.IResponseMessage;

/**
 * 武将上阵的返回消息
 * 
 * @author fengfeng
 *
 */
public class HeroUpTroop implements IResponseMessage {
	private long troopId1 = 0;
	private long troopGridId1 = 0;// 
	private int position1 = 0;// 
	
	private long troopId2 = 0;
	private long troopGridId2 = 0;// 
	private int position2 = 0;// 
	
	
	public HeroUpTroop(long troopId1, long troopGridId1, int position1) {
		super();
		this.troopId1 = troopId1;
		this.troopGridId1 = troopGridId1;
		this.position1 = position1;
	}
	
	public HeroUpTroop(long troopId1, long troopGridId1, int position1,
			long troopId2, long troopGridId2, int position2) {
		super();
		this.troopId1 = troopId1;
		this.troopGridId1 = troopGridId1;
		this.position1 = position1;
		this.troopId2 = troopId2;
		this.troopGridId2 = troopGridId2;
		this.position2 = position2;
	}



	public long getTroopId1() {
		return troopId1;
	}
	public void setTroopId1(long troopId1) {
		this.troopId1 = troopId1;
	}
	public long getTroopGridId1() {
		return troopGridId1;
	}
	public void setTroopGridId1(long troopGridId1) {
		this.troopGridId1 = troopGridId1;
	}
	public int getPosition1() {
		return position1;
	}
	public void setPosition1(int position1) {
		this.position1 = position1;
	}
	public long getTroopId2() {
		return troopId2;
	}
	public void setTroopId2(long troopId2) {
		this.troopId2 = troopId2;
	}
	public long getTroopGridId2() {
		return troopGridId2;
	}
	public void setTroopGridId2(long troopGridId2) {
		this.troopGridId2 = troopGridId2;
	}
	public int getPosition2() {
		return position2;
	}
	public void setPosition2(int position2) {
		this.position2 = position2;
	}

	

}
