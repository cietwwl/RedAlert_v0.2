package com.youxigu.dynasty2.entity.domain;

import java.io.Serializable;

/**
 * 背包物品
 * @author Dagangzi
 *
 */
public class ItemExchange implements Serializable {
	private int exchangeId;//兑换id映射item中exchangeId
	private String needEntIds;//需要的实体id
	private String toEntIds;//兑换的实体id

	private int[][] needs;
	private int[][] tos;

	public int getExchangeId() {
		return exchangeId;
	}

	public void setExchangeId(int exchangeId) {
		this.exchangeId = exchangeId;
	}

	public String getNeedEntIds() {
		return needEntIds;
	}

	public void setNeedEntIds(String needEntIds) {
		this.needEntIds = needEntIds;
	}

	public String getToEntIds() {
		return toEntIds;
	}

	public void setToEntIds(String toEntIds) {
		this.toEntIds = toEntIds;
	}

	public int[][] getNeeds() {
		return needs;
	}

	public void setNeeds(int[][] needs) {
		this.needs = needs;
	}

	public int[][] getTos() {
		return tos;
	}

	public void setTos(int[][] tos) {
		this.tos = tos;
	}

}
