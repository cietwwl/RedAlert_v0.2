package com.youxigu.dynasty2.map.domain;

import java.io.Serializable;

/**
 * 动态资源刷新规则
 */
public class DynResCasRule implements Serializable {
    private static final long serialVersionUID = -5027805436594883404L;
    public static final int TYPE_PERSONAL_RES_CAS = 0;
    public static final int TYPE_NPC_RES_CAS = 1;

    private int id;
    private int stateId;
    private int type;//0:资源; 1:野怪npc
    private int level;//刷新的资源点的等级
    private int count;//刷新后需要达到的数量

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStateId() {
        return stateId;
    }

    public void setStateId(int stateId) {
        this.stateId = stateId;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
