package com.youxigu.dynasty2.map.domain;

import java.io.Serializable;
import java.sql.Timestamp;

/**
 * 记录开放的区块信息
 * 
 * @author fengfeng
 *
 */
public class OpenState implements Serializable {
	private static final long serialVersionUID = 3159598330277084124L;
	private int stateId;// 郡id
	private int mapCellId;// 占领的区块id
	private Timestamp createTimes;// 占领的时间

	public int getStateId() {
		return stateId;
	}

	public void setStateId(int stateId) {
		this.stateId = stateId;
	}

	public Timestamp getCreateTimes() {
		return createTimes;
	}

	public void setCreateTimes(Timestamp createTimes) {
		this.createTimes = createTimes;
	}

	public int getMapCellId() {
		return mapCellId;
	}

	public void setMapCellId(int mapCellId) {
		this.mapCellId = mapCellId;
	}

}
