package com.youxigu.dynasty2.user.domain;

/**
 * 君主等级对应属性的最大值定义
 *
 */

public class LvParaLimit implements java.io.Serializable {
	private static final long serialVersionUID = 7555378788960116638L;
	private int usrLv;
	private String tipTitle; // 升到此级发的邮件标题
	private String tipContent; // 升到此级发的邮件内容
	private int heroNumLimit;// 武将数量上限
	private int heroLvLimit;// 武将等级上限
	private int exp;// 君主坦克升级需要的经验
	private int actionPointLimit; // 行动力上限
	private int hpPointLimit; // 体力上限
	private int perHpPoint;// 每15分钟增加的体力值
	private int perActPoint;// 每15分钟增加的行动力值

	public int getUsrLv() {
		return usrLv;
	}

	public void setUsrLv(int usrLv) {
		this.usrLv = usrLv;
	}

	public String getTipTitle() {
		return tipTitle;
	}

	public void setTipTitle(String tipTitle) {
		this.tipTitle = tipTitle;
	}

	public String getTipContent() {
		return tipContent;
	}

	public void setTipContent(String tipContent) {
		this.tipContent = tipContent;
	}

	public int getHeroNumLimit() {
		return heroNumLimit;
	}

	public void setHeroNumLimit(int heroNumLimit) {
		this.heroNumLimit = heroNumLimit;
	}

	public int getHeroLvLimit() {
		return heroLvLimit;
	}

	public void setHeroLvLimit(int heroLvLimit) {
		this.heroLvLimit = heroLvLimit;
	}

	public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public int getActionPointLimit() {
		return actionPointLimit;
	}

	public void setActionPointLimit(int actionPointLimit) {
		this.actionPointLimit = actionPointLimit;
	}

	public int getHpPointLimit() {
		return hpPointLimit;
	}

	public void setHpPointLimit(int hpPointLimit) {
		this.hpPointLimit = hpPointLimit;
	}

	public int getPerHpPoint() {
		return perHpPoint;
	}

	public void setPerHpPoint(int perHpPoint) {
		this.perHpPoint = perHpPoint;
	}

	public int getPerActPoint() {
		return perActPoint;
	}

	public void setPerActPoint(int perActPoint) {
		this.perActPoint = perActPoint;
	}

}
